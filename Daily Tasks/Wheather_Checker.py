# TEMPERATURE CHECKER

#IF temperature is greater than 30 degrees say its a hot day 
#Otherwise IF it's less than 10 degrees say its a cold day
#Otherwise it's neither Hot nor Cold

# HOW TO USE: CHANGE TEMPERATURE DEGREE FIGURE AND RUN
Temperature = 300 

if Temperature >= 30:
    print("Its a Hot day")
    
elif Temperature < 10:
    print("It's a Cold day")
    
else:
    print("It's Neither hot nor Cold")
    
    